package com.vlc.dao;

public class Customer {
	    int customer_id ;
	    String name ;
	    int age ;
	    String gender ;
	    String type_of_employment ;
	    double salary ;
	    double balance ;
	    int existing_emis ;
	    long mobile_number ;
	    String email_id ;
	    
		public int getCustomer_id() {
			return customer_id;
		}
		public void setCustomer_id(int customer_id) {
			this.customer_id = customer_id;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getAge() {
			return age;
		}
		public void setAge(int age) {
			this.age = age;
		}
		public String getGender() {
			return gender;
		}
		public void setGender(String gender) {
			this.gender = gender;
		}
		public String getType_of_employment() {
			return type_of_employment;
		}
		public void setType_of_employment(String type_of_employment) {
			this.type_of_employment = type_of_employment;
		}
		public double getSalary() {
			return salary;
		}
		public void setSalary(double salary) {
			this.salary = salary;
		}
		public double getBalance() {
			return balance;
		}
		public void setBalance(double balance) {
			this.balance = balance;
		}
		public int getExisting_emis() {
			return existing_emis;
		}
		public void setExisting_emis(int existing_emis) {
			this.existing_emis = existing_emis;
		}
		public long getMobile_number() {
			return mobile_number;
		}
		public void setMobile_number(long mobile_number) {
			this.mobile_number = mobile_number;
		}
		public String getEmail_id() {
			return email_id;
		}
		public void setEmail_id(String email_id) {
			this.email_id = email_id;
		}
	    
	    
}
